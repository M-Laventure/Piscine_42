int		ft_any(char **tab, int(*f)(char*))
{
	int i;
	int j;
	int any;
	
	i = 0;
	any = 0;
	while (tab[i] != 0 && any == 0)
	{
		j = 0;
		while (tab[i][j] != '\0')
		{
			if (f(tab[i][j]) == 1)
				any = 1;
			j++;
		}
		i++;
	}
	return any;
}
