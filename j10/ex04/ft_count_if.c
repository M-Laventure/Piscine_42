int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int j;
	int count;

	i = 0;
	count = 0;
	while (tab[i] != 0)
	{
		j = 0;
		while (tab[i][j] != '\0')
		{
			if (f(tab[i][j]) == 1)
				count++;
			j++;
		}
		i++;
	}
	return (count);
}
