void	ft_putchar(char c);

void	ft_print_comb(void)
{
	int i;

	i = 12;
	while (i <= 789)
	{
		if (i / 100 < i / 10 % 10 && i / 10 % 10 < i % 10)
		{
			ft_putchar(48 + i / 100);
			ft_putchar(48 + i / 10 % 10);
			ft_putchar(48 + i % 10);
			if (i != 789)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
		}
		i = i + 1;
	}
}
