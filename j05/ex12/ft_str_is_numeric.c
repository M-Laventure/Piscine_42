int		ft_str_is_numeric(char *str)
{
	int i;
	int is_num;

	i = 0;
	is_num = 1;
	while (is_num == 1 && str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
		{
			is_num = 0;
		}
		i++;
	}
	return (is_num);
}
